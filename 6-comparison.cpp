#include <iostream>
using namespace std;

int main() {
    int x = 5, y = 10;

    cout << (x == y) << endl; // false because 5 is not equal to 10 so that makes sense right :D
    cout << (x != y) << endl; // true 
    cout << (x < y)  << endl; // true
    cout << (x >= y) << endl; // false 

    return 0;
}
